#Leer 3 notas de un estudiante, calcular la nota final (promedio de las tres notas)

nota1 = float(input("Digite la primer nota: "))
nota2 = float(input("Digite la seguna nota: "))
nota3 = float(input("Digite la tercera nota: "))
